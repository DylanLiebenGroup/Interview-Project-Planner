async function main() {
  const existing = JSON.parse(await readFile(join(DATA_DIR, 'projects.json'), 'utf-8'));
  const original12 = existing.slice(0, 12);
  const projectsOut = [...original12, ...NEW_PROJECTS];

  const tasksOut = [];
  for (const p of projectsOut) {
    const tpl = TASK_TEMPLATES[p.id];
    if (!tpl) throw new Error(`No task template for project ${p.id}`);
    if (tpl.pool.length < p.tasksTotal) {
      throw new Error(`Template pool for ${p.id} has ${tpl.pool.length}, need ${p.tasksTotal}`);
    }
    for (let i = 0; i < p.tasksTotal; i++) {
      const [title, description] = tpl.pool[i];
      tasksOut.push({
        id: `${p.id}_t${String(i + 1).padStart(3, '0')}`,
        projectId: p.id,
        title,
        description,
        status: i < p.tasksDone ? 'completed' : 'not_started',
      });
    }
  }

  await writeFile(join(DATA_DIR, 'projects.json'), JSON.stringify(projectsOut, null, 2) + '\n');
  await writeFile(join(DATA_DIR, 'tasks.json'), JSON.stringify(tasksOut, null, 2) + '\n');
  await writeFile(join(DATA_DIR, 'presence-seed.json'), JSON.stringify(PRESENCE_SEED, null, 2) + '\n');

  console.log(`Wrote ${projectsOut.length} projects and ${tasksOut.length} tasks.`);
}

main().catch((err) => { console.error(err); process.exit(1); });
